//*****************************************************************************
//   インクルードファイル
//*****************************************************************************
#include "main.h"
#include "scene.h"
#include "scene3D.h"
#include "3D_sample.h"
#include "texture.h"

//*****************************************************************************
//   定数定義
//*****************************************************************************
#define POLYGON3D_WIDTH  (20.0f)
#define POLYGON3D_HEIGHT (20.0f)

//=======================================================================================
//   コンストラクタ
//=======================================================================================
C3D_sample::C3D_sample():CScene3D(2)
{
}

//=======================================================================================
//   デストラクタ
//=======================================================================================
C3D_sample::~C3D_sample()
{
}

//=======================================================================================
//   初期化処理
//=======================================================================================
void C3D_sample::Init()
{
	// メンバ変数の初期化
	m_pos = D3DXVECTOR3( 0.0f, 0.0f, 0.0f);
	m_scl = D3DXVECTOR3(POLYGON3D_WIDTH, 0.0f, POLYGON3D_HEIGHT);

	// 親の初期化
	CScene3D::Init();

	// テクスチャセット
	m_pTexture = CTexture::GetTexture((int)CTexture::TEXTYPE_FIELD);

	// タイプの代入
	CScene::SetObjType(CScene::OBJTYPE_NONE);
}

//=======================================================================================
//   終了処理
//=======================================================================================
void C3D_sample::Uninit()
{
	CScene3D::Uninit();
}

//=======================================================================================
//   更新処理
//=======================================================================================
void C3D_sample::Update()
{
}

//=======================================================================================
//   描画処理
//=======================================================================================
void C3D_sample::Draw()
{
	CScene3D::Draw();
}

//==================================================================================================================================================
// 作成処理
//==================================================================================================================================================
C3D_sample *C3D_sample::Create(void)
{
	C3D_sample *pScene3D;                            // 変数宣言
	pScene3D = new C3D_sample;                       // 動的確保
	pScene3D->Init();                            // 初期化
	return pScene3D;                             // 値を返す
}